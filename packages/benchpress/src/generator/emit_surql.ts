/**
 * Seed → SurrealQL emitter.
 *
 * Output is consumable by `surreal import`. Statements use explicit record
 * IDs (e.g. `logs:log_00042`) so cross-table links — metrics.source_log_id,
 * completions.parent_id — resolve naturally.
 *
 * Datetimes are emitted as SurrealDB datetime literals: `d'2025-...Z'`.
 */
import type { Seed } from './persona.js';

export interface EmitSurqlOptions {
  seedVersion: string;
}

export function emitSurql(seed: Seed, opts: EmitSurqlOptions): string {
  const out: string[] = [];

  out.push(`-- benchpress canonical seed`);
  out.push(`-- seed_version: ${opts.seedVersion}`);
  out.push(`-- range: ${seed.startLocalDate} → ${seed.endLocalDate}  tz: ${seed.tz}`);
  out.push(`-- rows: logs=${seed.logs.length} metrics=${seed.metrics.length} entities=${seed.entities.length} todos=${seed.todos.length} completions=${seed.completions.length}`);
  out.push(`-- generated by packages/benchpress — do not edit by hand; regenerate via npm run generate`);
  out.push('');

  // ── user_entities ────────────────────────────────────────────────────
  out.push(`-- user_entities`);
  for (const e of seed.entities) {
    out.push(
      `CREATE user_entities:${e.id} CONTENT { ` +
        `name: ${str(e.name)}, ` +
        `kind: ${str(e.kind)}, ` +
        `data: ${obj(e.data)}, ` +
        `ts: ${dt(e.ts)}, ` +
        `local_date: ${str(e.local_date)}, ` +
        `local_tz: ${str(e.local_tz)} ` +
      `};`
    );
  }
  out.push('');

  // ── logs ──────────────────────────────────────────────────────────────
  out.push(`-- logs`);
  for (const l of seed.logs) {
    const parts = [
      `content: ${str(l.content)}`,
      `category: ${str(l.category)}`,
      `ts: ${dt(l.ts)}`,
      `local_date: ${str(l.local_date)}`,
      `local_tz: ${str(l.local_tz)}`,
    ];
    if (l.metadata) parts.push(`metadata: ${obj(l.metadata)}`);
    out.push(`CREATE logs:${l.id} CONTENT { ${parts.join(', ')} };`);
  }
  out.push('');

  // ── metrics ───────────────────────────────────────────────────────────
  out.push(`-- metrics`);
  for (const m of seed.metrics) {
    const parts = [
      `metric_name: ${str(m.metric_name)}`,
      `value: ${num(m.value)}`,
      `unit: ${str(m.unit)}`,
      `metric_type: ${str(m.metric_type)}`,
      `source: ${str(m.source)}`,
      `ts: ${dt(m.ts)}`,
      `local_date: ${str(m.local_date)}`,
      `local_tz: ${str(m.local_tz)}`,
    ];
    if (m.category) parts.push(`category: ${str(m.category)}`);
    if (m.source_text) parts.push(`source_text: ${str(m.source_text)}`);
    if (m.source_log_id) parts.push(`source_log_id: logs:${m.source_log_id}`);
    out.push(`CREATE metrics:${m.id} CONTENT { ${parts.join(', ')} };`);
  }
  out.push('');

  // ── user_data: todos ──────────────────────────────────────────────────
  out.push(`-- user_data: todos`);
  for (const t of seed.todos) {
    const dataInner: Record<string, unknown> = { title: t.title };
    if (t.description) dataInner.description = t.description;
    if (t.priority) dataInner.priority = t.priority;
    if (t.category) dataInner.category = t.category;
    if (t.recurrence) dataInner.recurrence = t.recurrence;
    const parts = [
      `type: ${str(t.type)}`,
      `status: ${str(t.status)}`,
      `data: ${obj(dataInner)}`,
      `ts: ${dt(t.ts)}`,
      `local_date: ${str(t.local_date)}`,
      `local_tz: ${str(t.local_tz)}`,
    ];
    if (t.due_at) parts.push(`due_at: ${dt(t.due_at)}`);
    out.push(`CREATE user_data:${t.id} CONTENT { ${parts.join(', ')} };`);
  }
  out.push('');

  // ── user_data: completions ────────────────────────────────────────────
  out.push(`-- user_data: completions`);
  for (const c of seed.completions) {
    const parts = [
      `type: ${str(c.type)}`,
      `status: 'completed'`,
      `parent_id: user_data:${c.parent_id}`,
      `data: ${obj(c.note ? { note: c.note } : {})}`,
      `ts: ${dt(c.ts)}`,
      `local_date: ${str(c.local_date)}`,
      `local_tz: ${str(c.local_tz)}`,
    ];
    out.push(`CREATE user_data:${c.id} CONTENT { ${parts.join(', ')} };`);
  }
  out.push('');

  return out.join('\n');
}

// ──────────────────────────────────────────────────────────────────────────
// Literal encoders. All output is plain SurrealQL — no $-vars.
// ──────────────────────────────────────────────────────────────────────────

function str(s: string): string {
  // SurrealDB single-quoted strings; escape backslashes and single quotes.
  return `'${s.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
}

function num(n: number): string {
  if (!Number.isFinite(n)) throw new Error(`non-finite number in seed: ${n}`);
  return Number.isInteger(n) ? n.toString() : n.toString();
}

function dt(isoUtc: string): string {
  // d'YYYY-MM-DDTHH:MM:SS.sssZ' — SurrealDB datetime literal.
  return `d'${isoUtc}'`;
}

function obj(o: Record<string, unknown>): string {
  const entries = Object.entries(o).map(([k, v]) => `${k}: ${value(v)}`);
  return `{ ${entries.join(', ')} }`;
}

function value(v: unknown): string {
  if (v === null || v === undefined) return 'NONE';
  if (typeof v === 'string') return str(v);
  if (typeof v === 'number') return num(v);
  if (typeof v === 'boolean') return v ? 'true' : 'false';
  if (Array.isArray(v)) return `[${v.map(value).join(', ')}]`;
  if (typeof v === 'object') return obj(v as Record<string, unknown>);
  throw new Error(`unsupported value in seed: ${typeof v}`);
}
